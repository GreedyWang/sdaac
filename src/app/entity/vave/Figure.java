package app.entity.vave;

public class Figure {
		private String figureNo;
		private float saving;
		private float count_;
		public String getFigureNo() {
			return figureNo;
		}
		public void setFigureNo(String figureNo) {
			this.figureNo = figureNo;
		}
		public float getSaving() {
			return saving;
		}
		public void setSaving(float saving) {
			this.saving = saving;
		}
		public float getCount_() {
			return count_;
		}
		public void setCount_(float count_) {
			this.count_ = count_;
		}
}
